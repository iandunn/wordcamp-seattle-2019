		<h1 class="entry-title">Code of Conduct</h1>	</header>

	<div class="entry-content">
		<ol class="ol-h3">
<li>
<h3>Purpose</h3>
<p>WordCamp Seattle&nbsp;believes our community should be truly open for everyone. As such, we are committed to providing a friendly, safe and welcoming environment for all, regardless of gender, sexual orientation, disability, ethnicity, religion, preferred operating system, programming language, or text editor.</p>
<p>This code of conduct outlines our expectations for participant behavior as well as the consequences for unacceptable behavior.</p>
<p>We invite all sponsors, volunteers, speakers, attendees, and other participants to help us realize a safe and positive conference experience for everyone.</p></li>
<li>
<h3>Open Source Citizenship</h3>
<p>A supplemental goal of this code of conduct is to increase open source citizenship by encouraging participants to recognize and strengthen the relationships between what we do and the community at large.</p>
<p>In service of this goal,<br>
WordCamp Seattle organizers will be taking nominations for exemplary citizens throughout the event and will recognize select participants after the conference on the website.</p>
<p>If you see someone who is making an extra effort to ensure our community is welcoming, friendly, and encourages all participants to contribute to the fullest extent, we want to know.<br>
You can nominate someone at the Registration table or on the <a href="https://2019.seattle.wordcamp.org/contact/">WordCamp Website</a>.</p></li>
<li>
<h3>Expected Behavior</h3>
<ul>
<li>Be considerate, respectful, and collaborative.</li>
<li>Refrain from demeaning, discriminatory or harassing behavior and speech.</li>
<li>Be mindful of your surroundings and of your fellow participants. Alert conference organizers if you notice a dangerous situation or someone in distress.</li>
<li>Participate in an authentic and active way. In doing so, you help to create<br>
WordCamp Seattle and make it your own.</li>
</ul>
</li>
<li>
<h3>Unacceptable Behavior</h3>
<p>Unacceptable behaviors include: intimidating, harassing, abusive, discriminatory, derogatory or demeaning conduct by any attendees of&nbsp;WordCamp Seattle and related events. All<br>
WordCamp Seattle venues may be shared with members of the public; please be respectful to all patrons of these locations.</p>
<p>Harassment includes: offensive verbal comments related to gender, sexual orientation, race, religion, disability; inappropriate use of nudity and/or sexual images in public spaces (including presentation slides); deliberate intimidation, stalking or following; harassing photography or recording; sustained disruption of talks or other events; inappropriate physical contact, and unwelcome sexual attention.</p></li>
<li>
<h3>Consequences Of Unacceptable Behavior</h3>
<p>Unacceptable behavior will not be tolerated whether by other attendees, organizers, venue staff, sponsors, or other patrons of&nbsp;WordCamp Seattle venues.</p>
<p>Anyone asked to stop unacceptable behavior is expected to comply immediately.</p>
<p>If a participant engages in unacceptable behavior, the conference organizers may take any action they deem appropriate, up to and including expulsion from the conference without warning or refund.</p></li>
<li>
<h3>What To Do If You Witness Or Are Subject To Unacceptable Behavior</h3>
<p>If you are subject to unacceptable behavior, notice that someone else is being subject to unacceptable behavior, or have any other concerns, please notify a conference organizer as soon as possible.</p>
<p>The&nbsp;WordCamp Seattle team will be available to help participants contact venue security or local law enforcement, to provide escorts, or to otherwise assist those experiencing unacceptable behavior to feel safe for the duration of the conference.&nbsp;Volunteers will be wearing WordCamp T-Shirts. Any volunteer can connect you with a conference organizer. You can also come to the special registration desk in the lobby and ask for the organizers.</p></li>
<li>
<h3>Scope</h3>
<p>We expect all conference participants (sponsors, volunteers, speakers, attendees, and other guests) to abide by this code of conduct at all conference venues and conference-related social events.</p></li>
<li>
<h3>Contact Information</h3>
<p>You may contact any of the organizers by going to the Happiness Bar and asking for one of&nbsp;them.</p></li>
