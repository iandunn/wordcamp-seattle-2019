<p>WordCamp Seattle is returning to the Washington State Conference Center on November 9th and 10th! A place for everyone involved within the WordPress ecosystem, from website developers to end users, this is an opportunity to reach this amazing, diverse community.</p>



<p>Last year’s event brought on over 500 paid attendees, 39 speakers, 25 sponsors, and a few dozen volunteers to the conference. </p>



<p>We have specifically designed the layout of the event space to create maximum visibility for our sponsors. Sponsor tables are set up in the main common area with the center serving as a hub for informal conversations, meals, and networking.</p>



<p>With coffee, tea, and snacks served at the far end of the space, we’ve made sure every booth is highly visible and will see foot traffic through the conference. </p>



<p>Our sponsor packages include an option to fit your budget, and we’d love to have you be part of our community!:</p>



<h3>Packages</h3>



<ul><li>Freelancer ($100)</li><li>Small Business ($250)</li><li>Bronze ($1,000)</li><li>Silver, ($2,500)</li><li>Gold ($5,000). </li></ul>



<p>For more information about becoming a sponsor and what each level includes, please <a href="https://2019.seattle.wordcamp.org/sponsors/sponsorship/">visit our Sponsorship page</a>.</p>
