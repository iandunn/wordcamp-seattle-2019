<h2>You (yes you!) are invited to apply to speak at WordCamp Seattle 2019!</h2>



<p>Perhaps you have a passion for WordPress or you want to share your WordPress-related technological or business insights and expertise. </p>



<p>Maybe you want to tell the story of how WordPress powers your dream, drives your business or transforms your professional or personal life. If so, why not apply to be a speaker?</p>



<p>WordCamp Seattle is a 100% community-driven event, which means an opportunity for everyone to both teach and learn, listen and speak. Everyone has a story to tell and even if you haven’t considered it before, we encourage you to apply.</p>



<p>Successful talks span a range of WordPress-related topics from development and design to open source, business, marketing, and more. We are looking for speakers who can teach, inform, and inspire.</p>



<p>As a speaker, you’ll receive complementary access to the event including lunch. More importantly, you’ll join a community of developers, designers and leaders who come together to make WordCamp Seattle happen.</p>



<h3>Speaking and application details</h3>



<p>This year, we will be accepting applications for both full (max 45 minute) and lightning (max 10 minute) sessions.  In addition, if you would like to lead a more hands-on session, you can apply to lead a 2-hour workshop on any WordPress topic that will introduce participants to a new skill or technique.  Please indicate your preferred format when you apply.</p>



<p>Please use the <a href="/apply-to-speak/">Apply to Speak form</a> to submit your application. The call for speakers closes at midnight PST on August 18th.</p>



<p>You are welcome to submit multiple proposals, up to a maximum of three. If you submit more than three, we will consider only your first three submissions.</p>



<p>Be sure your talk description/abstract defines the exact topic you want to talk about and what you hope people will learn or gain from attending your session.</p>



<h3>How speakers are selected</h3>



<p>A committee of volunteer organizers will review submissions beginning on August 19th. Applicants will be notified regarding the status of their application beginning on or around August 22nd. If you don’t hear from us immediately on the 22nd, please be patient as it may take some time to inform all applicants of their status. If you submit multiple proposals, you will only receive one response from us.</p>



<div class="wp-block-button"><a class="wp-block-button__link" href="/apply-to-speak/">Apply To Speak</a></div>



<p>WordCamp Seattle thrives as a result of many talented and inspiring speakers from many backgrounds. Even if you’ve never done anything like this before we invite you to <a href="/apply-to-speak/">apply</a> to join us as a speaker at WordCamp Seattle this year.</p>
